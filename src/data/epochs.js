export function getEpochAtTime(timeBYA) {
  for (let i = EPOCHS.length - 1; i >= 0; i--) {
    if (timeBYA <= EPOCHS[i].timeBYA) return EPOCHS[i];
  }
  return EPOCHS[0];
}

export function getEpochProgress(timeBYA) {
  return Math.max(0, Math.min(1, 1 - timeBYA / EPOCHS[0].timeBYA));
}
